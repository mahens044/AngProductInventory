export class Products{
  Name : string;
  Description:string;
  Manufacturer :string;
  Price : string;
  Quantity : string ;

  constructor(Name, Description, Manufacturer, Price, Quantity){
    this.Name = Name;
    this.Description = Description;
    this.Manufacturer = Manufacturer;
    this.Price = Price ;
    this.Quantity = Quantity;
  }

}
